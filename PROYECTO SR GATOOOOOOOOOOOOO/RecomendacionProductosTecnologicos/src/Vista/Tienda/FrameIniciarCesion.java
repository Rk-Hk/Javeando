    public Usuario BuscarUsuario(String usua,String pass){
        for(int i=0;i<=lsusu.size();i++){
            Usuario usuaux=lsusu.getDato(i);
            if(usuaux.getUser().equalsIgnoreCase(usua)&& usuaux.getPassword().equalsIgnoreCase(pass)){
                usuarioregistrado =usuaux;
            }
        }
        return usuarioregistrado;
        
    }

