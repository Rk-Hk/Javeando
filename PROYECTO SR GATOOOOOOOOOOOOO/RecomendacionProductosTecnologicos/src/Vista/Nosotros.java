    public boolean ArchivarReclamos(Reclamos recla){
         Reclamos p = recla;
         boolean estado = false;
         try{
             if(!archivo.exists()){
                 ObjectOutputStream salida = new ObjectOutputStream(new FileOutputStream(archivo));
                 salida.writeObject(p);
                 salida.close();
                 estado=true;
             }
             else{
                 MiObjectOutputStream salida = new MiObjectOutputStream(new FileOutputStream(archivo,true));
                 salida.writeUnshared(p);
                 salida.close();
                 estado = true;
             }
         }catch(IOException e){
             System.out.println("error");
         }
         
         return estado;
    }
    
    public ColaListaGenerica<Reclamos> MostrarReclamos(){
        ColaListaGenerica<Reclamos> lsrecla = new ColaListaGenerica<>();
        try{
            ObjectInputStream entrada = new ObjectInputStream(new FileInputStream(archivo));
            Reclamos recla ;

            while((recla = (Reclamos)entrada.readObject())!=null){
                lsrecla.Insertar(recla);
            }
            entrada.close();
        }catch(EOFException e){
            System.out.println();
        }catch(ClassNotFoundException e){
            System.out.println(e+"CLASE NO ENCONTRADA");
        }catch(IOException e){
            System.out.println(e+"ERROR AL LEER EL ARCHIVO");
        }
        return lsrecla;
    }
    

