    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
         
        Reclamos recla =new Reclamos();
        try{
        if(Integer.parseInt(jTextField3.getText())>40000000 && Integer.parseInt(jTextField3.getText())<99999999){
            recla.setDni(Integer.parseInt(jTextField3.getText()));
        }

        recla.setRazon(jTextField1.getText());
        recla.setFecha(jTextField2.getText());
        recla.setSujerencia(jTextArea5.getText());
        
        if(recla.getDni()==0){
            JOptionPane.showMessageDialog(null, "Revice el campo del DNI !!  ", "Registro Incompleto", JOptionPane.ERROR_MESSAGE);
        }else{
            boolean ok = ArchivarReclamos(recla);
            if(ok)
               JOptionPane.showMessageDialog(this,"EL RECLAMO HA SIDO ARCHIVADO CORRECTAMENTE !!"
                    + "\n Solucionaremos el inconveniente !!","Reclamo",JOptionPane.WARNING_MESSAGE);
                }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e+ "NO DEJE CAMPOS VACIOS", "ERROR DE REGISTRO", JOptionPane.YES_OPTION);
        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

