    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        boolean modificado=false;
        int fila = jTable2.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(this,"Selecciones una fila","ALERTA",JOptionPane.WARNING_MESSAGE);
        }
        else {

            String serieTabla = jTable2.getValueAt(fila,0).toString();
            int ordenComplemento= GestorPrincipal.getGestorComple().OrdenComplemento(serieTabla);

            usu.setSerie(jTextField1.getText());
            usu.setMarca(jComboBox1.getSelectedItem().toString());

            usu.setNombre(jComboBox2.getSelectedItem().toString());
            usu.setDescripcion(jTextArea1.getText());
            usu.setPrecio(Double.parseDouble(jTextField8.getText()));

            modificado = GestorPrincipal.getGestorComple().ModificarComplementoConFoto(usu,ordenComplemento);

            // modificando la tabla de Impresoras
            //serie
            jTable2.setValueAt(jTextField1.getText(),fila,0);
            //nombre
            jTable2.setValueAt(jComboBox2.getSelectedItem().toString(),fila,1);
            //marca
            jTable2.setValueAt(jComboBox1.getSelectedItem().toString(),fila,2);
            //modelo
            jTable2.setValueAt(jTextField8.getText(),fila,4);

            if(modificado){
                JOptionPane.showMessageDialog(this,"Moficacion de los Datos Exitoso!!!!");}

            // limpiamos todo

            limpiarCampos();
            this.jTextField1.requestFocus();
            jButton6.setEnabled(true);
            jButton1.setEnabled(true);
        }

    }//GEN-LAST:event_jButton4ActionPerformed

