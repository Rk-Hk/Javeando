    public void CargandoTablaTotal(){
        ArrayList<Compra> lsTotal = Dao.getDaoCompra().CuentaTotal();
        Compra con=null;
        double total=0;
   if(lsTotal.size()==-1) {
        JOptionPane.showMessageDialog(this,"No hay ningun Registro !!","Contabilidad",JOptionPane.WARNING_MESSAGE);
    }
   else 
   {  for(int i=0;i<lsTotal.size();i++){
       con=lsTotal.get(i);
     jTable1.setValueAt(con.getNombreProducto(),i,0);
     jTable1.setValueAt(con.getTotal(),i,1);
      total = total + con.getTotal();
   }
   jTextField1.setText(String.valueOf(total));
      
   }
    }
